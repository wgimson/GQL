package gqlMain;

public class Node {
    ////////////////////////////////////////////////////////////////////////////
    // MEMBER VARIABLES
    ////////////////////////////////////////////////////////////////////////////
    private String nodeName;
    private double nodeDouble;
    
    ////////////////////////////////////////////////////////////////////////////
    // CONSTRUCTORS
    ////////////////////////////////////////////////////////////////////////////
    public Node() {
        this.nodeName = null;
    }
    
    public Node(String name) {
        this.nodeName = name;
    }
    
    ////////////////////////////////////////////////////////////////////////////
    // GETTERS
    ////////////////////////////////////////////////////////////////////////////
    public String getName() {
        return this.nodeName;
    }

    public double getNodeDouble() {
        return this.nodeDouble;
    }

    ////////////////////////////////////////////////////////////////////////////
    // SETTERS
    ////////////////////////////////////////////////////////////////////////////
    public void setName(String name) {
        this.nodeName = name;
    }

    public void setNodeDouble(double nd) {
        this.nodeDouble = nd;
    }
    
    public String toString() {
        return "\nNode: " + this.nodeName + "; Number: " + this.nodeDouble + "\n";
    }
}
